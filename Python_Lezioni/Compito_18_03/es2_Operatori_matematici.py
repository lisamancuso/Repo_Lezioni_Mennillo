a = 18
b = 5

somma = a + b
sottrazione = a - b
moltiplicazione = a * b
divisione = a / b
divisione_intera = a // b
resto = a % b
potenza = a ** b

print(f"Il risultato della somma è {somma}.")
print(f"Il risultato della sottrazione è {sottrazione}.")
print(f"Il risultato della moltiplicazione è {moltiplicazione}.")
print(f"Il risultato della divisione è {divisione}.")
print(f"Il risultato della divisione intera è {divisione_intera}.")
print(f"Il resto è {resto}.")
print(f"Il risultato di {a} elevato a {b} è {potenza}.")